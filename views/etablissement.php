            <h1>Présentation:</h1>
            <p>
                Le lycée Clos Maire, situé au cœur de la ville de Beaune, accueille environ 980 élèves sur deux sites séparés de quelques mètres. Dans un cadre naturel et matériel agréable et à taille humaine, les personnels se mobilisent pour permettre à chaque élève de construire un parcours scolaire réussi et ambitieux.
                <br /><br />
                La polyvalence des structures d’accueil, la qualité d’enseignement et de l’accompagnement, la disponibilité et le dynamisme de la vie scolaire, l’unité et le rayonnement de l’établissement, contribuent à atteindre cet engagement.
                <br />
                Plus particulièrement, une démarche d’accueil et de prise en charge individuelle est mise en place à l’entrée du lycée en seconde générale et professionnelle : adaptation méthodologique, aide personnalisée dans le domaine scientifique, littéraire et en langues, approfondissement en groupes de projets, aide à l’orientation…
                <br /><br />
                Dans le cadre du projet vie scolaire et éducatif, les élèves construisent leur autonomie et leur responsabilité : délégués, Maison des Lycéens, Conseil de la Vie Lycéenne, prévention, vivre ensemble…
                <br /><br />
                L’établissement se mobilise également grâce au projet culturel pour permettre aux élèves de découvrir différents aspects de la culture et de l’environnement : atelier théâtre, cinéma, arts plastiques, partenariats avec les entreprises, échanges linguistiques et projets européens…
                <br />
                Hébergement : un service d’internat en chambres est proposé aux élèves ainsi qu’un self.            
            </p>
