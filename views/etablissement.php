            <h1>Projet:</h1>
            <p>
                <strong>Le projet d'établissement</strong> est élaboré en commun par les  différents partenaires et adopté par le conseil d'administration : il  définit les modalités particulières de mise en œuvre des&nbsp;orientations et  programmes nationaux. Il permet à chaque établissement de contribuer  aux objectifs nationaux de réussite des élèves en tenant compte de la  diversité des publics scolaires. Il exprime les choix pédagogiques et  éducatifs de l'établissement.<br /><br />
                <span style="color: #ff6600;"><strong>Le projet d’établissement du lycée CLOS MAIRE est un acte de volonté :</strong></span><br /><br />
                Volonté de dégager une politique d’établissement fondée sur des valeurs partagées qui se transmettent par des actes et donnent du sens à chaque action :<br />
                <span style="color: #ff6600;"><strong>►</strong></span>Équité et Disponibilité ;<br />
                <span style="color: #ff6600;"><strong>►</strong></span>Responsabilité et Travail ;<br />
                <span style="color: #ff6600;"><strong>►</strong></span>Efficacité et Attractivité ;<br />
                <span style="color: #ff6600;"><strong>►</strong></span>Identité et Unité.<br /><br />
                <span style="color: #ff6600;"><strong>Ces valeurs guident la démarche de projet dans les trois champs d’intervention : </strong></span><br />
                <span style="color: #ff6600;"><strong>►</strong></span> Champ de la réussite des élèves ;<br />
                <span style="color: #ff6600;"><strong>►</strong></span> Champ de la construction citoyenne ;<br />
                <span style="color: #ff6600;"><strong>►</strong></span>Champ de l’identité de l’établissement.<br /><br />
                Le projet d’établissement s’articule avec le projet académique et avec le contrat d’objectifs. Il s’inscrit naturellement dans le cadre des missions fondamentales de l’École : instruire, c’est-à-dire transmettre des connaissances et une culture ; éduquer, c’est-à-dire former le futur adulte et le futur citoyen dans une société démocratique et enfin préparer à un projet professionnel.<br>
                De par sa finalité et ses objectifs généraux, c’est un acte d’ambition dont la finalité est de permettre à chaque élève de s’inscrire dans un parcours scolaire et personnel ambitieux et réussi.
           </p>
